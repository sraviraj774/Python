print("Datatypes And Comment")
# Different types of data. Mainly there is 4 types of data - Integer (Int), Float, String, Boolean.

# Integer - All integers from -infinity to +infinity comes under integers.
print(100000)
print(10000)
print(1000)

# To check type of data just type the type functionality >> print(type(data))
print(type(123456))
print("-------------------------------------------")

# Float - All numbers with decimals comes under Float.
print(100000.5)
print(10000.5)
print(1000.5)

# To check type of data just type the type functionality >> print(type(data))
print(type(123456.5))
print("-------------------------------------------")

# String - Things under double quote "" or Single quote '' comes under string.
print("A")
print("a")
print("Ravi Raj Singh")

# To check type of data just type the type functionality >> print(type(data))
print(type("A"))
print("-------------------------------------------")

# Boolean - Means Binary 0 or 1. True for 1 and False for 0.
print(True)
print(False)
print("true")

# To check type of data just type the type functionality >> print(type(data))
print(type("True"))
print(type(True))
print(type(False))
print("-------------------------------------------")

# Comments - Writing by following #. Example- # My name is Ravi Raj.

# Functionalities in print Statement
